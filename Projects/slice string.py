#Reverse a String using Slicing
#We can reverse a string using slicing by providing the step value as -1.


s = 'HelloWorld'
reverse_str = s[::-1]
print(reverse_str)



s1 = s[8:1:-1]
print(s1)

s1 = s[-4:-2]
print(s1)
